#include <iostream>
#include <cmath>
using namespace std;

int main() {
  cout << "\nSquare root of 64 is: " << sqrt(64) << "\n";
  cout << "\nRound 2.6 to the nearest integer: " << round(2.6) << "\n";
  cout << "\nThe natural logarithm of 2 is: " <<log(8) << "\n";
  cout << "\nThe maximum number is : 20, 50" << max(20, 50);
  cout << "\nThe minimum number is : 20, 50" << min(20, 50);

  return 0;
}